def _esc(v) -> str:
    """Échappe une valeur possiblement None/non-str."""
    return html.escape(str(v)) if v not in (None, "") else ""


def _dt(v) -> str:
    """Format lisible d'une date (str ISO ou date), '—' si absente."""
    if not v:
        return "—"
    return html.escape(str(v))


def _ul(items: list | None, empty: str = "—") -> str:
    """Liste HTML à partir d'une liste de chaînes (champ `lines` du bloc engagement)."""
    items = [i for i in (items or []) if str(i).strip()]
    if not items:
        return f"<p>{html.escape(empty)}</p>"
    lis = "".join(f"<li>{html.escape(str(i))}</li>" for i in items)
    return f'<ul class="compact">{lis}</ul>'


def _para(v, empty: str = "—") -> str:
    """Paragraphe(s) à partir d'un champ texte libre (retours ligne préservés)."""
    if not v or not str(v).strip():
        return f"<p>{html.escape(empty)}</p>"
    parts = [html.escape(p.strip()) for p in str(v).split("\n") if p.strip()]
    return "".join(f"<p>{p}</p>" for p in parts)


def _kv_table(rows: list[tuple[str, str]]) -> str:
    """Tableau clé/valeur pour les blocs de métadonnées."""
    trs = "".join(
        f"<tr><td>{html.escape(k)}</td><td>{v or '—'}</td></tr>" for k, v in rows
    )
    return f'<table class="kv"><tbody>{trs}</tbody></table>'


def _sig_block(prestataire: str, client: str, langue: str = "fr") -> str:
    """Bloc de signatures côte à côte (prestataire / client)."""
    line = html.escape(_t(langue, "sig_line"))
    provider = html.escape(_t(langue, "sig_provider").format(name=prestataire or "Prestataire"))
    client_lbl = html.escape(_t(langue, "sig_client").format(name=client))
    return (
        '<div class="sig-row">'
        f'<div class="sig-box"><b>{provider}</b>{line}</div>'
        f'<div class="sig-box"><b>{client_lbl}</b>{line}</div>'
        "</div>"
    )


def _mask_if_secret(item: dict, langue: str = "fr") -> str:
    """Une preuve `contains_secrets` n'apparaît jamais en clair dans un rapport."""
    if item.get("contains_secrets"):
        return f'<span class="masked">{html.escape(_t(langue, "ev_masked"))}</span>'
    fallback = _t(langue, "ev_fallback_name")
    return html.escape(item.get("caption") or item.get("original_filename", fallback))


def _human_size(n, langue: str = "fr") -> str:
    """Taille lisible (o/Kio/Mio/Gio en fr, B/KiB/MiB/GiB en en), '—' si absente."""
    if n in (None, ""):
        return "—"
    try:
        size = float(n)
    except (TypeError, ValueError):
        return "—"
    units = _SIZE_UNITS.get(langue, _SIZE_UNITS["fr"])
    for i, unit in enumerate(units):
        if size < 1024 or unit == units[-1]:
            return f"{size:.0f} {unit}" if i == 0 else f"{size:.1f} {unit}"
        size /= 1024
    return f"{size:.1f} {units[-1]}"


def _ts(v) -> str:
    """Horodatage forensique complet (date + heure UTC), '—' si absent."""
    if not v:
        return "—"
    s = str(v)
    # 'YYYY-MM-DDTHH:MM:SS...' ou 'YYYY-MM-DD HH:MM:SS...' → 'YYYY-MM-DD HH:MM:SS UTC'
    s = s.replace("T", " ")
    if "." in s:
        s = s.split(".", 1)[0]
    elif "+" in s:
        s = s.split("+", 1)[0]
    return html.escape(s.strip() + " UTC")


def _evidence_register(evidence: list[dict], langue: str = "fr") -> str:
    """Registre de preuves façon investigation numérique : une ligne par preuve avec
    horodatage, nom, type MIME, empreinte SHA-256, taille et déposant. Le nom des preuves
    `contains_secrets` est masqué ; les métadonnées d'intégrité (empreinte, taille,
    horodatage, déposant) restent visibles — elles ne révèlent pas le contenu."""
    if not evidence:
        return f"<p>{html.escape(_t(langue, 'ev_empty'))}</p>"
    rows = ""
    for e in evidence:
        sha = e.get("sha256_plaintext")
        sha_cell = f'<span class="mono">{html.escape(sha)}</span>' if sha else "—"
        rows += (
            "<tr>"
            f"<td class='mono nowrap'>{_ts(e.get('uploaded_at'))}</td>"
            f"<td>{_mask_if_secret(e, langue)}</td>"
            f"<td class='mono'>{_esc(e.get('detected_mime')) or '—'}</td>"
            f"<td class='hash'>{sha_cell}</td>"
            f"<td class='nowrap'>{_human_size(e.get('size_bytes'), langue)}</td>"
            f"<td>{_esc(e.get('uploaded_by_name')) or '—'}</td>"
            "</tr>"
        )
    return (
        '<table class="evidence-register"><thead><tr>'
        f"<th>{html.escape(_t(langue, 'ev_th_ts'))}</th>"
        f"<th>{html.escape(_t(langue, 'ev_th_name'))}</th>"
        f"<th>{html.escape(_t(langue, 'ev_th_mime'))}</th>"
        f"<th>{html.escape(_t(langue, 'ev_th_hash'))}</th>"
        f"<th>{html.escape(_t(langue, 'ev_th_size'))}</th>"
        f"<th>{html.escape(_t(langue, 'ev_th_by'))}</th>"
        f"</tr></thead><tbody>{rows}</tbody></table>"
    )


