def _vuln_details(findings: list[dict], evidence: list[dict],
                  previews: dict[str, str], langue: str) -> str:
    """Fiche détaillée par vulnérabilité : toutes les métadonnées saisies par l'auditeur
    + tableau des preuves associées (liées par finding_id, référencées par empreinte) avec
    aperçu inline pour les images non secrètes."""
    if not findings:
        return f"<p>{html.escape(_t(langue, 'vd_none'))}</p>"
    # Regroupement des preuves par vulnérabilité (finding_id).
    ev_by_finding: dict[str, list[dict]] = {}
    for e in evidence:
        fid = e.get("finding_id")
        if fid:
            ev_by_finding.setdefault(str(fid), []).append(e)

    cards = ""
    for f in findings:
        sev = str(f.get("severite") or "info").lower()
        sub_bits = [b for b in (_esc(f.get("cve")), _esc(f.get("cwe")),
                                (f"TLP:{_esc(f.get('tlp'))}" if f.get("tlp") else "")) if b]
        sub = " · ".join(sub_bits)

        # Métadonnées : uniquement les champs renseignés (fiche lisible).
        epss = _pct(f.get("epss"))
        epss_pct = _pct(f.get("epss_percentile"))
        epss_val = None
        if epss:
            epss_val = epss + (f" (percentile {epss_pct})" if epss_pct else "")
        kev_val = None
        if f.get("kev"):
            kev_val = _t(langue, "yes") + (
                f" · {_t(langue, 'vd_kev_ransom')}" if f.get("kev_ransomware") else ""
            )
        candidate_rows = [
            (_t(langue, "vd_kv_cvss"), _esc(f.get("cvss_score"))),
            (_t(langue, "vd_kv_vector"), _esc(f.get("cvss_vector"))),
            ("OWASP Top 10", _esc(f.get("owasp_top10"))),
            (_t(langue, "vd_kv_status"), _esc(f.get("statut"))),
            (_t(langue, "vd_kv_exploit"), _esc(f.get("exploitabilite"))),
            (_t(langue, "vd_kv_phase"), _esc(f.get("phase_decouverte"))),
            (_t(langue, "vd_kv_discoverer"), _esc(f.get("decouvreur_nom"))),
            (_t(langue, "vd_kv_sla"), _esc(f.get("sla_niveau"))),
            (_t(langue, "vd_kv_sla_due"), _dt(f.get("sla_echeance")) if f.get("sla_echeance") else ""),
            (_t(langue, "vd_kv_epss"), epss_val or ""),
            (_t(langue, "vd_kv_kev"), kev_val or ""),
            (_t(langue, "vd_kv_ssvc"), _esc(f.get("ssvc_decision"))),
            (_t(langue, "vd_kv_vex"), _esc(f.get("vex_status"))),
        ]
        kv_rows = [(k, v) for k, v in candidate_rows if v]
        meta_kv = _kv_table(kv_rows) if kv_rows else ""

        techniques = f.get("techniques") or []
        ttp = "".join(f'<span class="pill">{_esc(t)}</span>' for t in techniques)
        ttp_html = f"<h4>{_t(langue, 'vd_h_ttp')}</h4><p>{ttp}</p>" if ttp else ""
        d3 = f.get("d3fend") or []
        d3_pills = "".join(f'<span class="pill">{_esc(x)}</span>' for x in d3)
        d3_html = f"<h4>{_t(langue, 'vd_h_d3fend')}</h4><p>{d3_pills}</p>" if d3_pills else ""

        ev_items = ev_by_finding.get(str(f.get("id")), [])
        cards += (
            '<div class="vuln-card">'
            f'<h3>{_esc(f.get("titre")) or "—"} '
            f'<span class="sev-badge {sev}">{_esc(f.get("severite")) or sev}</span></h3>'
            + (f'<p class="sub">{sub}</p>' if sub else "")
            + meta_kv
            + _vuln_prose(f, "description", "vd_h_desc", langue)
            + _vuln_prose(f, "impact_metier", "vd_h_impact", langue)
            + _vuln_prose(f, "preuve_exploitation", "vd_h_proof", langue)
            + _vuln_prose(f, "recommandation", "vd_h_reco", langue)
            + ttp_html + d3_html
            + f"<h4>{_t(langue, 'vd_h_evidence')}</h4>"
            + _vuln_evidence_table(ev_items, previews, langue)
            + "</div>"
        )
    return cards


def render_ptes_report(*, client: dict, prestataire: dict | None, audit: dict,
                       scenario: dict | None, applications: list[dict],
                       auditeurs: list[dict], actions: list[dict],
                       findings: list[dict], evidence: list[dict], tlp: str,
                       langue: str = "fr", previews: dict[str, str] | None = None,
                       preview_max_bytes: int = 0) -> str:
    """Rapport PTES complet. Les preuves secrètes restent masquées (porte 5)."""
    previews = previews or {}
    client_name = client.get("nom", "")
    presta_name = (prestataire or {}).get("nom") or "Prestataire Purple Team"
    referentiels = ", ".join(audit.get("referentiels_methodo") or []) or "PTES"

    # ── Synthèse chiffrée par sévérité ── (les libellés de sévérité restent des codes métier)
    sev_order = ["critique", "haute", "moyenne", "basse", "info"]
    sev_count: dict[str, int] = {}
    for f in findings:
        s = str(f.get("severite") or "info").lower()
        sev_count[s] = sev_count.get(s, 0) + 1
    stat_cells = "".join(
        f"<td class='sev-{s}'><strong>{sev_count.get(s, 0)}</strong> {s}</td>"
        for s in sev_order if s in sev_count
    ) or f"<td>{_t(langue, 'rpt_no_vuln')}</td>"

    # ── Vulnérabilités détaillées ──
    rows = "".join(
        f"<tr><td>{_esc(f.get('titre'))}"
        + (f"<br><span class='meta'>{_esc(f.get('cve'))}</span>" if f.get('cve') else "")
        + f"</td><td>{_esc(f.get('cvss_score')) or '—'}</td>"
        f"<td class='sev-{_esc(str(f.get('severite','')).lower())}'>{_esc(f.get('severite')) or '—'}</td>"
        f"<td>{_esc(f.get('epss')) or '—'}</td>"
        f"<td>{_t(langue, 'yes') if f.get('kev') else '—'}</td>"
        f"<td>{_esc(f.get('sla_niveau')) or '—'}</td>"
        f"<td>{_esc(f.get('statut')) or '—'}</td></tr>"
        for f in findings
    )

    # ── Actions d'audit par phase PTES ──
    actions_html = ""
    if actions:
        act_rows = "".join(
            f"<tr><td>{_esc(a.get('ptes_phase')) or '—'}</td><td>{_esc(a.get('titre'))}</td>"
            f"<td>{_esc(a.get('technique_attack')) or '—'}</td>"
            f"<td>{_esc(a.get('outil')) or '—'}</td>"
            f"<td>{_esc(a.get('resultat')) or '—'}</td></tr>"
            for a in actions
        )
        actions_html = (
            f"<h2>{_t(langue, 'rpt_h_actions')}</h2>"
            f"<table><thead><tr><th>{_t(langue, 'rpt_th_phase')}</th><th>{_t(langue, 'rpt_th_action')}</th>"
            f"<th>ATT&amp;CK</th><th>{_t(langue, 'rpt_th_tool')}</th>"
            f"<th>{_t(langue, 'rpt_th_result')}</th></tr></thead><tbody>{act_rows}</tbody></table>"
        )

    # ── Contexte scénario ──
    scenario_html = ""
    if scenario:
        techniques = scenario.get("techniques") or []
        tech_pills = "".join(f'<span class="pill">{_esc(t)}</span>' for t in techniques[:16])
        more = f" <span class='meta'>(+{len(techniques)-16})</span>" if len(techniques) > 16 else ""
        scenario_html = (
            f"<h2>{_t(langue, 'rpt_h_scenario')}</h2>"
            + _kv_table([
                (_t(langue, "rpt_kv_scenario"), _esc(scenario.get("nom"))),
                (_t(langue, "rpt_kv_actor"), _esc(scenario.get("acteur_emule")) or "—"),
                (_t(langue, "rpt_kv_sophis"), _esc(scenario.get("sophistication")) or "—"),
                (_t(langue, "rpt_kv_objective"), _esc(scenario.get("objectif")) or "—"),
            ])
            + (f"<p>{tech_pills}{more}</p>" if tech_pills else "")
        )

    # ── Périmètre applicatif ──
    app_rows = "".join(
        f"<tr><td>{_esc(a.get('nom'))}</td><td>{_esc(a.get('criticite')) or '—'}</td>"
        f"<td>{_esc(a.get('exposition')) or '—'}</td><td>{_esc(a.get('stack')) or '—'}</td></tr>"
        for a in applications
    )
    apps_html = (
        f"<h2>{_t(langue, 'rpt_h_apps')}</h2><table><thead><tr><th>{_t(langue, 'rpt_th_app')}</th>"
        f"<th>{_t(langue, 'rpt_th_crit')}</th><th>{_t(langue, 'rpt_th_expo')}</th>"
        f"<th>{_t(langue, 'rpt_th_stack')}</th></tr></thead><tbody>{app_rows}</tbody></table>"
    ) if app_rows else ""

    team = ", ".join(_esc(r.get("nom")) for r in auditeurs) or "—"
    evidence_register = _evidence_register(evidence, langue)
    categorie_type_test = _esc(audit.get("categorie")) + (
        f" · {_esc(audit.get('type_test'))}" if audit.get("type_test") else ""
    )
    mission_kv = _kv_table([
        (_t(langue, "rpt_kv_client"), _esc(client_name)),
        (_t(langue, "rpt_kv_provider"), _esc(presta_name)),
        (_t(langue, "rpt_kv_cat"), categorie_type_test),
        (_t(langue, "rpt_kv_refs"), _esc(referentiels)),
        (_t(langue, "rpt_kv_period"), f"{_dt(audit.get('date_debut'))} → {_dt(audit.get('date_fin'))}"),
        (_t(langue, "rpt_kv_env"), _esc(audit.get("environnement")) or "—"),
        (_t(langue, "rpt_kv_team"), team),
        (_t(langue, "rpt_kv_status"), _esc(audit.get("statut")) or "—"),
    ])
    report_meta = _t(langue, "rpt_meta").format(
        nom=_esc(audit.get("nom")), client=_esc(client_name), presta=_esc(presta_name)
    )
    vuln_rows = rows or f"<tr><td colspan=7>{_t(langue, 'rpt_none')}</td></tr>"
    vuln_details_html = _vuln_details(findings, evidence, previews, langue)
    preview_limit_note = ""
    if preview_max_bytes:
        preview_limit_note = (
            f'<p class="meta">{_t(langue, "vd_preview_limit").format(size=_human_size(preview_max_bytes, langue))}</p>'
        )
    footer = _t(langue, "footer_client").format(tlp=_esc(tlp))

    return f"""<!DOCTYPE html><html><head><meta charset="utf-8">{_BASE_CSS}</head><body>
    {_banner(tlp, client_name)}
    <h1>{_t(langue, 'rpt_title')}</h1>
    <p class="meta">{report_meta}</p>

    <h2>{_t(langue, 'rpt_h_mission')}</h2>
    {mission_kv}

    <h2>{_t(langue, 'rpt_h_summary')}</h2>
    <table><tbody><tr>{stat_cells}</tr></tbody></table>

    {scenario_html}
    {apps_html}
    {actions_html}

    <h2>{_t(langue, 'rpt_h_vulns')}</h2>
    <table><thead><tr><th>{_t(langue, 'rpt_th_vuln')}</th><th>CVSS</th>
    <th>{_t(langue, 'rpt_th_sev')}</th><th>EPSS</th>
    <th>KEV</th><th>SLA</th><th>{_t(langue, 'rpt_th_status')}</th></tr>
    </thead><tbody>{vuln_rows}</tbody></table>

    <h2>{_t(langue, 'vd_h_section')}</h2>
    {preview_limit_note}
    {vuln_details_html}

    <h2>{_t(langue, 'rpt_h_evidence')}</h2>
    {evidence_register}
    <p class="meta">{_t(langue, 'rpt_evidence_note')}</p>

    <div class="footer">{footer}</div>
    </body></html>"""


async def html_to_pdf(html_content: str) -> bytes:
    """Convertit le HTML en PDF via le service Chromium headless (pdf-renderer).

    En cas d'échec du renderer, on LÈVE une erreur au lieu de renvoyer le HTML :
    stocker du HTML sous une extension .pdf produit un fichier corrompu (le bug
    observé). Le pipeline reste testable via un renderer réel ou un mock explicite.
    """
    import httpx

    from app.config import settings

    renderer_url = getattr(settings, "pdf_renderer_url", "http://pdf-renderer:3000/pdf")
    async with httpx.AsyncClient(timeout=30) as http:
        resp = await http.post(renderer_url, json={"html": html_content})
        resp.raise_for_status()  # 503 du renderer → RuntimeError remonté à l'appelant
        ctype = resp.headers.get("content-type", "")
        if "application/pdf" not in ctype:
            # Garde-fou : ne jamais laisser passer autre chose qu'un vrai PDF.
            raise RuntimeError(
                f"pdf-renderer a renvoyé {ctype!r} au lieu d'un PDF — rendu indisponible"
            )
        return resp.content


# ── Micro-service de rendu (service `pdf-renderer` du compose) ───────────────
# Le modèle DOIT être défini au niveau module : un BaseModel déclaré dans une
# closure (à l'intérieur de _build_render_app) n'est pas reconnu de façon fiable
# par FastAPI comme corps de requête — il le traite alors en paramètre query,
# renvoyant 422 "Field required (query, req)" même sur un POST JSON valide.
# C'était la cause du 422 systématique du renderer.
class RenderRequest(BaseModel):
    # Corps attendu : {"html": "<...>"}. Champ tolérant (défaut vide) : le contrôle
    # "html vide" est fait explicitement dans le endpoint, avec un message clair.
    html: str = ""


def _build_render_app():
    """Application FastAPI minimale exposant POST /pdf.

    Rend un vrai PDF via Playwright/Chromium. En cas d'indisponibilité du moteur,
    renvoie une erreur explicite (503) — JAMAIS du HTML déguisé en PDF (qui serait
    stocké tel quel sous une extension .pdf). Cette séparation garde le rendu hors
    du processus API (isolation, ressources).
    """
    render_app = FastAPI(title="pdf-renderer")

    @render_app.get("/health")
    async def _health():
        return {"status": "ok"}

    @render_app.post("/pdf")
    async def _pdf(req: RenderRequest):
        html_content = req.html
        if not html_content.strip():
            return Response(
                content=b"empty_html", media_type="text/plain", status_code=422
            )
        try:
            from playwright.async_api import async_playwright  # type: ignore

            async with async_playwright() as p:
                browser = await p.chromium.launch(args=["--no-sandbox"])
                page = await browser.new_page()
                await page.set_content(html_content, wait_until="networkidle")
                pdf = await page.pdf(format="A4", print_background=True)
                await browser.close()
            return Response(content=pdf, media_type="application/pdf")
        except Exception as exc:
            # NE JAMAIS renvoyer le HTML déguisé en succès : l'appelant le
            # stockerait sous .pdf (fichier corrompu). Échec franc → l'API
            # remonte l'erreur au lieu de produire un faux PDF.
            import logging

            logging.getLogger("pdf-renderer").error(
                "rendu Chromium indisponible (%s) — vérifier `playwright install "
                "chromium` dans l'image", exc, exc_info=True,
            )
            return Response(
                content=b"pdf_renderer_unavailable",
                media_type="text/plain",
                status_code=503,
            )

    return render_app


if __name__ == "__main__":
    import uvicorn

    uvicorn.run(_build_render_app(), host="0.0.0.0", port=3000)
